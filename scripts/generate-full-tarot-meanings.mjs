function buildMeaning(card, orientation) {
  const reversed = orientation === "reversed";
  const core = reversed ? card.revCore : card.core;
  const keywords = reversed ? card.revKeywords : card.keywords;
  const warning = reversed
    ? `${card.name_ko} 역방향은 ${card.revCore}을 보여주지만, 단순한 실패로 단정하지 말고 부족, 과잉, 지연, 왜곡, 내면화 중 무엇이 질문에 맞는지 구분해야 합니다.`
    : `${card.name_ko} 정방향은 ${card.core}을 보여주지만, 결과를 단정하지 말고 질문의 위치에 맞게 적용해야 합니다.`;

  return {
    keywords,
    traditional_meaning: `${card.name_ko} ${reversed ? "역방향" : "정방향"}은 ${core}을 뜻합니다.`,
    symbolism: card.symbols,
    positive_aspect: reversed
      ? `${card.revCore}을 자각하면 패턴을 수정하고 다시 균형을 잡을 수 있습니다.`
      : `${card.core}을 정확히 이해하면 질문의 핵심을 현실적으로 읽을 수 있습니다.`,
    warning,
    must_include: keywords.slice(0, 4),
    common_mistakes: [
      `${card.name_ko}을 질문과 무관한 대표 키워드로만 해석함`,
      "결과를 반드시 일어난다고 단정함",
      "정통 의미보다 현실 조언을 앞세움",
      "역방향을 단순 반대나 무조건 실패로만 해석함",
    ],
  };
}

function buildContexts(card) {
  const contexts = {};
  for (const [key, label] of Object.entries(contextLabels)) {
    contexts[key] = {
      upright: `${label} 질문에서는 ${card.name_ko}의 ${card.core}이 ${domainApplication(key)}에서 어떤 확인 항목으로 드러나는지 좁혀 읽습니다.`,
      reversed: `${label} 질문에서는 ${card.name_ko} 역방향의 ${card.revCore}이 ${domainApplication(key)}에서 부족, 과잉, 지연, 왜곡 중 무엇으로 드러나는지 구분해 읽습니다.`,
    };
  }
  return contexts;
}

