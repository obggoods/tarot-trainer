function cardToJson(card) {
  return {
    card_id: card.id,
    name_ko: card.name_ko,
    name_en: card.name_en,
    arcana: card.arcana,
    number: card.number,
    suit: card.suit,
    rank: card.rank,
    meanings: {
      upright: buildMeaning(card, "upright"),
      reversed: buildMeaning(card, "reversed"),
    },
    contexts: buildContexts(card),
  };
}

function writeJson(relativePath, data) {
  const filePath = path.join(meaningsRoot, relativePath);
  fs.mkdirSync(path.dirname(filePath), { recursive: true });
  fs.writeFileSync(filePath, `${JSON.stringify(data, null, 2)}\n`, "utf8");
}

function toImportName(id) {
  return id.replace(/[^a-zA-Z0-9]/g, "_");
}

