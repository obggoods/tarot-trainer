for (const [suit, suitDef] of Object.entries(suitCards)) {
  for (const [rank, rankKo, name_en, core, revCore, keywords, revKeywords, symbols] of suitDef.cards) {
    const numberMap = { ace: 1, page: 11, knight: 12, queen: 13, king: 14 };
    const number = numberMap[rank] ?? Number(rank);
    const id = `${suit}_${rank}`;
    allCards.push({
      id,
      name_ko: `${suitDef.ko} ${rankKo}`,
      name_en,
      arcana: "minor",
      suit,
      rank,
      number,
      core,
      revCore,
      keywords,
      revKeywords,
      symbols,
      theme: suitDef.theme,
      file: `minor/${suit}/${rank}.json`,
    });
  }
}

for (const card of allCards) {
  writeJson(card.file, cardToJson(card));
}

